class Product {
  final int id;
  final String brand;
  final String name;
  final String price;
  final String imageLink;
  final String description;
  final String productType;
  final String productLink;   

  Product({
    required this.id,
    required this.brand,
    required this.name,
    required this.price,
    required this.imageLink,
    required this.description,
    required this.productType,
    required this.productLink,  
  });
}
